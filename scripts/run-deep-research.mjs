#!/usr/bin/env node
// Deep Research Max runner — kicks off one state, polls until done, saves report.
//
// Usage:
//   node scripts/run-deep-research.mjs <state>
//   node scripts/run-deep-research.mjs california
//
// Resumes if interrupted (saves interaction id to reports/<state>.id):
//   node scripts/run-deep-research.mjs california --resume

import { readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");

// Load .env.local
const envPath = resolve(ROOT, ".env.local");
if (existsSync(envPath)) {
  for (const line of readFileSync(envPath, "utf8").split("\n")) {
    const m = line.match(/^([A-Z_][A-Z0-9_]*)=(.*)$/);
    if (m && !process.env[m[1]]) process.env[m[1]] = m[2];
  }
}

const API_KEY = process.env.GEMINI_API_KEY;
if (!API_KEY) {
  console.error("Missing GEMINI_API_KEY (set in .env.local)");
  process.exit(1);
}

const args = process.argv.slice(2);
const state = args.find((a) => !a.startsWith("--"));
const resume = args.includes("--resume");
if (!state) {
  console.error("Usage: node scripts/run-deep-research.mjs <state> [--resume]");
  process.exit(1);
}

const stateTitle = state[0].toUpperCase() + state.slice(1).toLowerCase();
const reportsDir = resolve(ROOT, "reports");
mkdirSync(reportsDir, { recursive: true });
const reportPath = resolve(reportsDir, `${state.toLowerCase()}.md`);
const idPath = resolve(reportsDir, `${state.toLowerCase()}.id`);
const metaPath = resolve(reportsDir, `${state.toLowerCase()}.json`);

const promptTemplate = readFileSync(
  resolve(ROOT, "scripts/prompts/state-research.md"),
  "utf8"
);
const input = promptTemplate.replaceAll("{STATE}", stateTitle);

const API = "https://generativelanguage.googleapis.com/v1beta/interactions";
const headers = {
  "Content-Type": "application/json",
  "x-goog-api-key": API_KEY,
};

async function api(method, path, body) {
  const url = path ? `${API}/${path}` : API;
  const res = await fetch(url, {
    method,
    headers,
    body: body ? JSON.stringify(body) : undefined,
  });
  const text = await res.text();
  if (!res.ok) {
    throw new Error(`HTTP ${res.status} ${method} ${url}\n${text}`);
  }
  return JSON.parse(text);
}

async function createInteraction() {
  console.log(`Submitting Deep Research Max for ${stateTitle}…`);
  const body = {
    input,
    agent: "deep-research-max-preview-04-2026",
    background: true,
    agent_config: {
      type: "deep-research",
      thinking_summaries: "auto",
    },
  };
  const r = await api("POST", "", body);
  console.log(`  interaction id: ${r.id}`);
  console.log(`  status:         ${r.status}`);
  writeFileSync(idPath, r.id);
  return r.id;
}

async function poll(id) {
  const startedAt = Date.now();
  let lastStatus = "";
  let dots = 0;
  while (true) {
    const elapsed = ((Date.now() - startedAt) / 60000).toFixed(1);
    let r;
    try {
      r = await api("GET", id);
    } catch (e) {
      console.error(`\npoll error: ${e.message}`);
      await sleep(30000);
      continue;
    }
    if (r.status !== lastStatus) {
      process.stdout.write(`\n[${elapsed}m] status=${r.status}`);
      lastStatus = r.status;
      dots = 0;
    } else {
      process.stdout.write(".");
      if (++dots % 20 === 0) process.stdout.write(` [${elapsed}m]`);
    }

    if (r.status === "completed") {
      console.log("\n— done —");
      return r;
    }
    if (r.status === "failed" || r.status === "cancelled") {
      console.log("\n— terminated —");
      console.log(JSON.stringify(r.error || {}, null, 2));
      process.exit(2);
    }
    if (Date.now() - startedAt > 75 * 60 * 1000) {
      console.log("\nTimed out after 75 min. Re-run with --resume to keep polling.");
      process.exit(3);
    }
    await sleep(30000);
  }
}

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

function extractText(result) {
  const outs = result.outputs || [];
  // Find the last text output (the final report)
  for (let i = outs.length - 1; i >= 0; i--) {
    const o = outs[i];
    if (typeof o.text === "string" && o.text.trim()) return o.text;
    if (Array.isArray(o.content)) {
      const t = o.content.find((c) => c.type === "text" && c.text);
      if (t) return t.text;
    }
  }
  return null;
}

(async () => {
  let id;
  if (resume && existsSync(idPath)) {
    id = readFileSync(idPath, "utf8").trim();
    console.log(`Resuming poll on ${id}`);
  } else {
    id = await createInteraction();
  }

  const result = await poll(id);
  const text = extractText(result);
  if (!text) {
    console.error("No text output found. Dumping full result for inspection.");
    writeFileSync(metaPath, JSON.stringify(result, null, 2));
    process.exit(4);
  }

  const header = `# Small Claims in ${stateTitle}\n\n_Generated by Gemini Deep Research Max on ${new Date().toISOString()}_\n\n---\n\n`;
  writeFileSync(reportPath, header + text);
  writeFileSync(
    metaPath,
    JSON.stringify(
      {
        id,
        state: stateTitle,
        agent: "deep-research-max-preview-04-2026",
        usage: result.usage,
        completedAt: new Date().toISOString(),
      },
      null,
      2
    )
  );

  console.log(`\nReport saved: ${reportPath}`);
  console.log(`Meta saved:   ${metaPath}`);
  if (result.usage) {
    console.log(`Tokens — input: ${result.usage.input_tokens}  output: ${result.usage.output_tokens}  total: ${result.usage.total_tokens}`);
  }
})().catch((e) => {
  console.error(e);
  process.exit(1);
});
